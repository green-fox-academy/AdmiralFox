public class Plant {

    private String color;
    private int waterLevel;

    public Plant() {
        this.color = color;
        this.waterLevel = waterLevel;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getWaterLevel() {
        return waterLevel;
    }

    public void setWaterLevel(int waterLevel) {
        this.waterLevel = waterLevel;
    }
}
